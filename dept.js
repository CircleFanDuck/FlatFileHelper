function getDeptDetail(key){
    if(!trimed){
	    deptMapOri = deptMap;
		deptMap = {};
		for(var p in deptMapOri){
			deptMap[jQuery.trim(p)]=jQuery.trim(deptMapOri[p]);
		}
		trimed = true;
	}
	if(deptMap[key]){
	    return deptMap[key];
	}else{
	    return '<label class=error>Not exist!</label>'
	}
}

